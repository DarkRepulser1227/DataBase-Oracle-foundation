--事务 (Transaction)
--事务是一条或多条的sql语句构成的执行序列，这个序列中的素有语句都属于一个工作单元，用于完成一个特定的业务逻辑
--事务中的语句要么都执行，要么一条也不做，目的是保证数据的一致性和完整性
--事务的特征 ACID
--1.A（Atomicity）：原子性
--2.C(Consistency):一致性，从一个一致性状态变成另一个一致性状态
--3.I（Isolation）:隔离性：不能被干扰
--4.D(Durability):持久性，一旦提交，对数据的改变是永久性
--Oracle的事务分为显示事务和隐式事务
--显示提交 commit
--隐士事务，当第一条DML执行时，事务就默认开始了
--事务结束1）显示提交2）DML之后跟着DDL或DCL，3）当会话关闭时，默认提交 4）当系统异常终止或崩溃，默认回滚
--和事务相关的关键字
--1）commit：提交
--2）rollback：回滚
--3）savepoint：保存点
